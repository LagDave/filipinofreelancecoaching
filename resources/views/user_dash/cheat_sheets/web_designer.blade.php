<p><strong>Description:</strong><span style="font-weight: 400;"> A web designer develops and creates websites and associated applications. Web designers work in a variety of industries and often as independent contractors. Education requirements can vary, but web designers can get entry-level work with an associate's degree. Bachelor's degrees provide students with an expanded and advanced skill set that can lead to better job prospects or career advancement.</span></p>
<p>&nbsp;</p>
<p><strong>Job Description:</strong></p>
<p><span style="font-weight: 400;">A web designer creates the look, layout, and features of a website. The job involves understanding both graphic design and computer programming. Once a website is created, a designer helps with maintenance and additions to the website. They work with development teams or managers for keeping the site up-to-date and prioritizing needs, among other tasks.</span></p>
