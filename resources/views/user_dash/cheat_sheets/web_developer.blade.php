<p><strong>Web Design vs Web Development</strong></p>
<p>&nbsp;</p>
<p><span style="font-weight: 400;">Web design and web development are not the same, but of course, it depends on how you define &lsquo;design&rsquo; and &lsquo;development&rsquo;. For our article, we have assumed that it takes different knowledge to understand what is a web designer and what is a web developer.&nbsp;</span></p>
<p>&nbsp;</p>
<p><strong>These professions are different and they have the following roles:</strong></p>
<p>&nbsp;</p>
<p><strong>Web Designer:</strong></p>
<p>&nbsp;</p>
<p><span style="font-weight: 400;">The web designer is the person or group of people who are responsible for the creation of the website concept. They might decide that it needs to be a certain color, with certain content and pages. They may do things like creating infographics, logos, and videos, and they tell the developer where these things have to be put on the web page. However, they don&rsquo;t take part in the construction of the website or the underlying code.</span></p>
<p>&nbsp;</p>
<p><strong>Web Developer:</strong></p>
<p>&nbsp;</p>
<p><span style="font-weight: 400;">The web developer takes the designer&rsquo;s concepts and creates the code that is used to turn them into a website and bring them to people like you and me. It is important to realize that, although the web developer and the designer may be the same person &ndash; there is almost always some overlap between design and front-end development &ndash; the roles are different.</span></p>
<p>&nbsp;</p>
<p><span style="font-weight: 400;">If you want to be a designer, you won&rsquo;t necessarily have to learn any code at all. Likewise, a developer doesn&rsquo;t need to have any graphic design skills at all. From this, it follows that &lsquo;designing a website&rsquo; is not part of the answer to &lsquo;What is a web developer?&rsquo;.</span></p>
