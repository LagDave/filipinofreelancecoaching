<p><strong>Duties of a Graphics Designer:</strong></p>
<p><span style="font-weight: 400;">Graphic designers typically do the following:&nbsp;</span></p>
<p>&nbsp;</p>
<p><span style="font-weight: 400;">Meet with clients or the art director to determine the scope of a project</span></p>
<p><span style="font-weight: 400;">Advise clients on strategies to reach a particular audience</span></p>
<p><span style="font-weight: 400;">Determine the message the design should portray</span></p>
<p><span style="font-weight: 400;">Create images that identify a product or convey a message</span></p>
<p><span style="font-weight: 400;">Develop graphics for product illustrations, logos, and websites</span></p>
<p><span style="font-weight: 400;">Select colors, images, text style, and layout</span></p>
<p><span style="font-weight: 400;">Present the design to clients or the art director</span></p>
<p><span style="font-weight: 400;">Incorporate changes recommended by the clients into the final design</span></p>
<p><span style="font-weight: 400;">Review designs for errors before printing or publishing them</span></p>